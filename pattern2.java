package practice;
import java.util.*;
public class pattern2 {
	public static void main(String args[]) {
		Scanner inp = new Scanner(System.in);
		int a = inp.nextInt();
		for(int i=1;i<=a;i++,System.out.println("\n")); {
			for(int j=0;j<=a;j++) {
				System.out.print("*");
			}
				for(int j=1;j<=a;j++) {
					System.out.print(" ");
				}
			}
		}
	
}
