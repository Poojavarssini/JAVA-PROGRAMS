package practice;

import java.util.*;
public class pattern {
	public static void main(String args[]) {
		Scanner inp = new Scanner(System.in);
		int a = inp.nextInt();
		int cnt=1;
		for(int i=1;i<=a;i++) {
			for(int j=1;j<i;j++) {
				System.out.print(cnt+" ");
				cnt++;
				
			}
		System.out.println("");
		System.out.println();
			}
		
	}
	}
	